export function getTranslation(key: string, lang: string = 'ENGLISH_INTL', params: Record<string, any> = {}) {
  const selectedLang = (['DEUTSCH_EU', 'FRENCH_EU', 'ESPANOL_EU'].includes(lang) ? lang : 'ENGLISH_INTL') as Language;
  const translationSet = translations[selectedLang] as Record<string, string>;
  let text = translationSet[key] || translations['ENGLISH_INTL'][key as keyof typeof translations['ENGLISH_INTL']] || key;
  
  Object.keys(params).forEach(paramKey => {
    text = text.replace(`{{${paramKey}}}`, params[paramKey]);
  });
  
  return text;
}

export function useTranslation(lang: string = 'ENGLISH_INTL') {
  return (key: string, params: Record<string, any> = {}) => getTranslation(key, lang, params);
}

